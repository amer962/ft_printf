#include "ft_printf.h"

int	ft_unsigned(unsigned int n)

{
	char	a[10];
	int		i;
	int		x;

	i = 0;
	x = 0;
	if (n == 0)
		return (write(1, "0", 1));
	while (n > 0)
	{
		a[i] = (n % 10) + '0';
		n = n / 10;
		i++;
	}
	x = i;
	while (i--)
		write(1, &a[i], 1);
	return (x);
}
